# [미션 1] 짝수 추출 및 제곱 처리
# 목표: 입려 ㄱ리스트에서 짝수만 골라 제곱한 결과를 반환한다.
numbers = [1, 2, 3, 4, 5, 6]

# 리스트 컴프리헨션 구조 분해:
# [ (표현식) for (항목) in (반복 가능 객체) if (조건문) ]
# 1. 항목(n): numbers의 요소를 하나씩 꺼내온다.
# 2. 조건문(if n % 2 == 0): 짝수인 경우에만 다음 단계로 진행한다(필터링).
# 3. 표현식(n**2): 피렅링된 숫자를 제곱한다(매핑).
result = [n ** 2 for n in numbers if n % 2 == 0]

print(f"최종 결과 리스트: {result}")