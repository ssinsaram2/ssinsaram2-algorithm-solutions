# 03-1. 가변 언패킹(*)을 이용한 데이터 분리

# [실전 예시] 정렬된 점수 리스트에서 극단값 제외 및 중간 데이터 추출
# 데이터가 이미 정령되어 있다고 가정 (예: [70, 70, 80, 90, 100])
scores = [50, 70, 80, 90, 100]

# 처음(최소)와 끝(최대)을 추출하고 중간은 '*'을 통해 리스트로 묶음
min_val, *mid_vals, max_val = scores

print("\n--- 3.1 가변 언패킹 결과 ---")
print(f"최소: {min_val}, 최대: {max_val}, 분석 대상: {mid_vals}")